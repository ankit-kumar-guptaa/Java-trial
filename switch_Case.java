public class switch_Case {
    public static void main(String[] args) {
        int num = 400;
        switch (num) {
            case 20:
                System.out.println("case1");
                break;
            case 100:
                System.out.println("case2");
                break;
            case 300:
                System.out.println("case3");
                break;
                default: System.out.println("no match any case");
        }
    }

}
